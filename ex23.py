def print_line(line, encoding, errors):
    next_lang = line.strip()  # stripping '\n' from each line. Test this: print(len("kkkkkkAjmalkkk kkk".strip('k '))) here we are asking strip() to remove character k and space.
    raw_bytes = next_lang.encode(encoding, errors=errors)  # I pass errors to tell it how to handle errors  # https://www.programiz.com/python-programming/methods/string/encode
    cooked_string = raw_bytes.decode(encoding, errors=errors)  # use decode() to get string from raw bytes.
    print(raw_bytes, "<===>", cooked_string)


languages = open('languages.txt', encoding="utf-8")
main(languages, encoding, error)
languages.close()
# open the file languages.txt again in read mode and call the function main but this time use with.
with open("languages.txt") as languages:
    main(languages, encoding, error)

