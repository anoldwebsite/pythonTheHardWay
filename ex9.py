days = "Mon Tue Wed Thu Fri Sat Sun"
# Escape sequence \n for moving to new line
months = "Jan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"  # use \n for new line makes a string go across multiple lines.

print("Here are the days:", days)
print("Here are the months:", months)

